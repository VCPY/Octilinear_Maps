/**
 * Class containing filters for the drawing of the graph.
 */
export class Filters {
  /**
   * Array containing the names of the lines which should be drawn.
   */
  static exactString: string[] = []

  /**
   * Boolean to toggle if crossing of lines is allowed.
   */
  static ALLOWCROSSING: boolean = true

  /**
   * Scaling of graph.
   */
  static r: number = 0.5
}
